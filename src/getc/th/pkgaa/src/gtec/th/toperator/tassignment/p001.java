public class p001 {

	public static void main(String[] args) {
		int i=10,j=15;
		i+=j;
		System.out.println(i);
		i-=j;
		System.out.println(i);
		i*=j;
		System.out.println(i);
		i/=j;
		System.out.println(i);
		i%=j;
		System.out.println(i);
		i=10;
		i&=j;
		System.out.println(i);
		i=10;
		i|=j;
		System.out.println(i);
		i=10;
		i^=j;
		System.out.println(i);
		i=~10;
		System.out.println(i);
		i=10;
		i<<=3;
		System.out.println(i);
		i=10;
		i>>=3;
		System.out.println(i);
		
	

	}

}
