package gtec.th.pkgaa;

public class OprUna01 {

	public static void main(String[] args) {
	int i=5;
	System.out.println(i);
	System.out.println(++i);
	System.out.println(i);
	System.out.println(i++);
	System.out.println(i);
	System.out.println(--i);
	System.out.println(i);
	System.out.println(i--);
	System.out.println(i);

	}

}
